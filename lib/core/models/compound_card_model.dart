class CompoundCardModel {
  final String compoundName;
  final String compoundImage;
  final String compoundLocation;
  final int pricesStart;
  final int spaceStart;
  final double rate;
  final String compoundState;

  const CompoundCardModel({
    required this.compoundName,
    required this.compoundImage,
    required this.compoundLocation,
    required this.pricesStart,
    required this.spaceStart,
    required this.rate,
    required this.compoundState,
  });
}
